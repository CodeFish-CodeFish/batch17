package primitivePractice;

public class ArithmeticPractice2 {
    public static void main(String[] args) {
        // find the area of a triangle if base is 4.5ft, height is 7.8ft
        // area = 1/2 times base and height
        // A = 1/2 * b * h
        // A = b* h / 2

        double base = 4.5, height = 7.8, area;

        area = base * height / 2 ;

        System.out.println( "Area of triangle is "+ area + " square ft");

    }
}
