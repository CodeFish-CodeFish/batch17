package primitivePractice;
public class ArithmeticPractice {
    public static void main(String[] args){

        // Addition --> +

        int a = 9;
        int b = 6;

        int total = a + b;
        System.out.println(total + " is total of two numbers");

        int c = a + 1;
        System.out.println( "Value of c "+ c);

        // Subtraction --> -
        int result1 = total - b;
        System.out.println( "Result1 is "+ result1);

        // multiplication --> *
        int num1 = 8, num2 = 3;
        int result2 = num1 * num2;
        System.out.println("The result after multiplication is: "+result2);

        // Division --> /
        int car = 3;
        int result3 = car / 2;
        System.out.println("Count of car that I get is: "+ result3);
        double result4 = car/ 2;
        System.out.println("Count of car that I get is *** : "+ result4);

        double result5 = 3.0 / 2;

        System.out.println(result5);



    }
}
